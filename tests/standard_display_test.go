package chip8_test

import (
	"bytes"
	"testing"

	chip8 "github.com/MarceloMPJR/go-chip-8"
)

func TestStandardDisplay_Flush(t *testing.T) {
	output := &bytes.Buffer{}
	disp := chip8.NewStandardDisplay(&chip8.ConfigDisplay{Output: output})

	expected := initialScreen()

	disp.Flush()
	result := output.String()

	if result != expected {
		t.Errorf("result:\n%s\nexpected:\n%s\n", result, expected)
	}
}

func TestStandardDisplay_Clear(t *testing.T) {
	output := &bytes.Buffer{}
	disp := chip8.NewStandardDisplay(&chip8.ConfigDisplay{Output: output})

	expected := initialScreen()

	disp.Draw(5, 5, 0xFA)
	disp.Draw(5, 6, 0x90)
	disp.Draw(5, 7, 0x90)
	disp.Draw(5, 8, 0x90)
	disp.Draw(5, 9, 0xFA)
	disp.Clear()
	disp.Flush()
	result := output.String()

	if result != expected {
		t.Errorf("result:\n%s\nexpected:\n%s\n", result, expected)
	}
}

func TestStandardDisplay_Draw(t *testing.T) {
	t.Run("when collision do not occurs", func(t *testing.T) {
		output := &bytes.Buffer{}
		disp := chip8.NewStandardDisplay(&chip8.ConfigDisplay{Output: output})

		collision := false
		collision = collision || disp.Draw(5, 5, 0xF0)
		collision = collision || disp.Draw(5, 6, 0x80)
		collision = collision || disp.Draw(5, 7, 0xF0)
		collision = collision || disp.Draw(5, 8, 0x10)
		collision = collision || disp.Draw(5, 9, 0xF0)

		disp.Flush()
		result := output.String()
		expected := drawCharFive()

		if collision == true {
			t.Errorf("unexpected colission")
		}

		if result != expected {
			t.Errorf("result:\n%s\nexpected:\n%s\n", result, expected)
		}
	})

	t.Run("when collision occurs", func(t *testing.T) {
		output := &bytes.Buffer{}
		disp := chip8.NewStandardDisplay(&chip8.ConfigDisplay{Output: output})

		collision := false
		collision = collision || disp.Draw(5, 5, 0xF0)
		collision = collision || disp.Draw(5, 5, 0xF0)

		disp.Flush()

		result := output.String()
		expected := initialScreen()

		if collision == false {
			t.Errorf("expected colission, but do not occurs")
		}

		if result != expected {
			t.Errorf("result:\n%s\nexpected:\n%s\n", result, expected)
		}
	})
}

